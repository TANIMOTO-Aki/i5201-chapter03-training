public class EX3_Calc {
    public static void main(String[] args) {
        int a = 100,b = 30;                  // 変数a,bの宣言と初期値の代入

        System.out.println("100 * 30 =" + (a * b) );
        System.out.println("100 / 30 =" + ((double)(a) / b));
        System.out.println("100 + 30 =" + (a + b));
        System.out.println("100 - 30 =" + (a - b));
    }
}
